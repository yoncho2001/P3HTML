//Digit as word

let number = +prompt('number ?', '');

switch (number) {
    case 1:
        alert('one')
        break;
    case 2:
        alert('two')
        break;
    case 3:
        alert('three')
        break;
    case 4:
        alert('four')
        break;
    case 5:
        alert('five')
        break;
    case 6:
        alert('six')
        break;
    case 7:
        alert('seven')
        break;
    case 8:
        alert('8eight')
        break;
    case 9:
        alert('nine')
        break;
    case 0:
        alert('zero')
        break;

    default:
        alert('not a digit')
        break;
}